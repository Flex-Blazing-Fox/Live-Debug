<template>
  <a
    href="#"
    class="list-group-item list-group-item-action mb-1"
    @click.prevent="fetchDetail(card.card_number)"
  >
    <div class="d-flex w-100 justify-content-between">
      <div class="col-9">
        <h5 class="mb-1 text-left">{{ card.name }}</h5>
      </div>
      <div class="col-3">
        <small>{{ card.card_number }}</small>
      </div>
    </div>
    <p class="mb-1 col">{{ card.rarity }}</p>
  </a>
</template>

<script>
export default {
  name: 'SidebarListItem',
  props: {
    card: Object,
  },
  data() {
    return {
      card: {
        name: '',
        card_number: '',
        card_rarity: ''
      }
    }
  },
  methods: {
    fetchDetail(cardNumber) {
      this.$store
        .dispatch('fetchCardDetail', {
          card_number: cardNumber,
        })
        .then(({ data }) => {
          this.card.name = data.name,
          this.card.card_number = data.card_number,
          this.card_rarity = data.card_rarity
        })
        .catch();
    },
  },
};
</script>

